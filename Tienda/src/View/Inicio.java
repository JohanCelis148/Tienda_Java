    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String persona, producto, cantidad, valorUnitario;
        persona = jcbpersona.getSelectedItem().toString();
        List<String[]> informacion = new ArrayList<>();
        
        while (contador3 < contador ) {
            String[] data = new String[contador+2];
            
            producto = String.valueOf(tblTabla.getValueAt(contador2, 1));
            cantidad = String.valueOf(tblTabla.getValueAt(contador2, 2));
            valorUnitario = String.valueOf(tblTabla.getValueAt(contador2, 3));
            
            data [0] = producto;
            data [1] = cantidad;
            data [2] = valorUnitario;
            informacion.add(data);
            contador2++;
            contador3++;
        }
        
        DetalleFactura factura = new DetalleFactura(totalFactura, persona, informacion);
        factura.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnConsultarActionPerformed

