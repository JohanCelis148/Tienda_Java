    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        try {
            String producto, persona;
            Integer cantidad;
            producto = jcbproducto.getSelectedItem().toString();
            persona = jcbpersona.getSelectedItem().toString();
            
            if (!persona.equals("Seleccione") && !producto.equals("Seleccione")) {
                    cantidad = Integer.parseInt(txtcantidad.getText());
                    List<String[]> valorUnitario = new ArrayList<>();
                    Integer id = 0;
                    
                    Object [] fila = new Object[2]; 
                    fila[0]= producto; 
                    fila[1]= cantidad;
                    
                    int precioProducto = 0;
                    
                    valorUnitario = inicioC.ValorUnitario(producto);
                    
                    for (String[] dato : valorUnitario) {
                        precioProducto = Integer.parseInt(dato[2]);
                    }
                    
                    totalFactura = totalFactura + (cantidad * precioProducto);

                    tblTabla.setValueAt(codigo, contador, 0);
                    tblTabla.setValueAt(fila[0], contador, 1);
                    tblTabla.setValueAt(fila[1], contador, 2);
                    tblTabla.setValueAt(precioProducto, contador, 3);
                    contador++;
                    codigo++;

            } else {
                JOptionPane.showMessageDialog(null, "Por favor revise los datos ingresados");
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

